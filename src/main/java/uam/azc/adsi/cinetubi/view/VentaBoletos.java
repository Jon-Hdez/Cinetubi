    private void inicialidarbtg(){
        btgTipo.add(btnTarjeta);
        btgTipo.add(btnEfectivo);    
        txtCambio.setEditable(false);
        txtTotal.setEditable(false);
        txtTotal.setText(String.valueOf(taquillaController.getSubtotal()));
    }

    private void mostrar() {
        modo="efectivo";
        txtCambio.setVisible(true);
        txtPago.setVisible(true);
        lblCambio.setVisible(true);
        lblPago.setVisible(true);
    }

    private void ocultar() {
        modo="credito";
        txtCambio.setVisible(false);
        txtPago.setVisible(false);
        lblCambio.setVisible(false);
        lblPago.setVisible(false);
    }

    private double regresarCambio() {
        try {
            // Obt√©n el total como double en lugar de int
            double total = Double.parseDouble(txtTotal.getText()); 
            // Obt√©n el subtotal como double
            double subtotal = taquillaController.getSubtotal(); 
            // Calcula el cambio
            return total - subtotal; 
        } catch (NumberFormatException e) {
            System.err.println("Error al parsear el total: " + e.getMessage());
            return 0; // O manejar el error de otra manera
        }
    }

