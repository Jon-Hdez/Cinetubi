    private void btnProcesarVEntaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarVEntaActionPerformed
        // TODO add your handling code here:
        BoletoDAO bol=new BoletoDAO();
        if(regresarCambio()>=0 || modo.equalsIgnoreCase("credito")){
            int idVenta = ventaController.crearVenta(taquillaController.getEmpleado().getId(), -1, modo, "taquilla", taquillaController.getSubtotal());
            bol.agregarBoletos(ventaController.crearVentaBoletos(taquillaController, modo, "taquilla", taquillaController.getSubtotal()), idVenta);
            this.dispose();
            // Abrir el nuevo JFrame (Taquilla)
            Taquilla nuevaTaquilla = new Taquilla();
            nuevaTaquilla.setVisible(true);
        }
    }//GEN-LAST:event_btnProcesarVEntaActionPerformed

